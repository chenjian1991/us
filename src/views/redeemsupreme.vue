        if( this.firstName && this.lastName && this.email && this.phone && this.street && this.APT && this.city && this.country && this.postcode ){
          this.captchaIns && this.captchaIns.popUp()
        }else {
            this.isSubmit = true
            this.modalInfo = 'Please fill in the required infomation.'
          }
      },
      initRobot(){
              let _that = this;
              let captchaIns;
              if (captchaIns) {
                  captchaIns.destroy()
              }
              let lan = this.$store.state.app.countryLanguage;
              if(lan==='zh-CN'){
                      lan = 'zh-CN'
              }else{
                  lan = 'en'
              }
              initNECaptcha({
                  element: '#captcha',
                  captchaId: 'a3cd39c172284133a3470b7ec05a2bb0',
                  mode: 'popup',
                  width: '320px',
                  lang:lan,
                  onReady: function (instance) {
                      // 验证码一切准备就绪，此时可正常使用验证码的相关功能
                      //console.log('instance',instance)
                  },
                    onClose: function () {
                      // 弹出关闭结束后将会触发该函数
                      // console.log('close')
                      // _that.loaded = true;
                  },
                  onVerify: function (err, data) {
                      if(err){
                          //console.log('err',err)
                      }
                      if(data){
                          //console.log('validate',data.validate)
                          _that.verify(data.validate)
                          _that.initRobot()
                      }
                  }
              }, function (instance) {
                  // 初始化成功后，用户输入对应用户名和密码，以及完成验证后，直接点击登录按钮即可
                  _that.captchaIns = instance;
              }, function onerror(err) {
                  _that.captchaIns = ''
                  //验证码初始化失败处理逻辑，例如：提示用户点击按钮重新初始化
              })//初始化函数结尾
              
              return captchaIns;
          },
          verify(validate){
              //debugger
              postApplyCardInfo(validate,{
                  business:"redeemsupreme",
                  firstName:this.firstName,
                  lastName:this.lastName,
                  email:this.email,
                  phone:this.phone,
                  street:this.street,
                  APT:this.APT,
                  city:this.city,
                  country:this.country,
                  postcode:this.postcode
              }).then(data => {
                    this.isSubmit = true
                    this.modalInfo = 'Your message is well received!'
                }).catch(err => {
                    this.isSubmit = true
                    this.modalInfo = 'We can not send through your message for some reason, please re-check and send again.'
                })
          },
  },
  mounted(){
          this.initRobot();
      }
};
</script>
